
public class TerenaryOperator {

	public static void main(String[] args) {

		//terenary operator
		//syntax
		//  ?: -->condition?exp1:exp2
    
		int i = 3;
        int j =0;
        
       //scenario:if i>6,assign j=1 and else assign j = 2
        
    //?: -->condition?exp1:exp2
        j = i>6?1:2;
        
        System.out.println(j);
         


		
		
	}

}
